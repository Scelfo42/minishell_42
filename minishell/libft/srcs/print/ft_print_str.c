#include "libft.h"

/*!
 * @brief 
	This function writes a string char by char.
 * @param str
	The string to be printed.
 * @return
	The length of the printed string.
*/

int	ft_putstr(char *str)
{
	size_t	i;

	i = 0;
	if (!str)
		return (ft_putstr("(null)"));
	while (str[i])
		ft_putchar(str[i++]);
	return (i);
}
