#include "libft.h"

/*!
 * @brief 
	This function copies the content of a string into another.
 * @param dst
	The destination string.
 * @param src
	The source string.
 * @param size
	The number of bytes that will be copied.
 * @return
	The number of bytes inside src.
*/

size_t	ft_strlcpy(char *dst, char *src, size_t size)
{
	size_t	i;

	i = 0;
	if (size > 0)
	{
		while (src[i] && i < (size - 1))
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	while (src[i])
		i++;
	return (i);
}
