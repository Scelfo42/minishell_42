#include "libft.h"

/*!
 * @brief 
	This function converts a string (made of ASCII characters) into an int. 
 * @param str
	The string which contains, hypothetically, the number to be converted.
 * @return
	The number represented in int.
*/

int	ft_atoi(char *nptr)
{
	int		result;
	int		sign;
	int		i;

	result = 0;
	sign = 1;
	i = 0;
	while ((nptr[i] == ' ') || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '-')
		sign *= -1;
	if (nptr[i] == '-' || nptr[i] == '+')
		i++;
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		result = (result * 10) + nptr[i] - '0';
		i++;
	}
	return (result * sign);
}
