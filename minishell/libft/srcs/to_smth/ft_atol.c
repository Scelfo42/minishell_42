#include "libft.h"

/*!
 * @brief 
	This function converts a string (made of ASCII characters) into a long int. 
 * @param str
	The string which contains, hypothetically, the number to be converted.
 * @return
	The number represented in long int.
*/

long int	ft_atol(char *str)
{
	long int	result;
	int			sign;
	int			i;

	result = 0;
	sign = 1;
	i = 0;
	while ((str[i] == ' ') || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
		sign *= -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = (result * 10) + str[i] - 48;
		i++;
	}
	return (result * sign);
}
